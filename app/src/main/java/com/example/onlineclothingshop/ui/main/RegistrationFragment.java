    public static RegistrationFragment newInstance(String param1, String param2) {
        RegistrationFragment fragment = new RegistrationFragment();
        Bundle args = new Bundle();
        args.putString(ARG_PARAM1, param1);
        args.putString(ARG_PARAM2, param2);
        fragment.setArguments(args);
        return fragment;
    }

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        if (getArguments() != null) {
            mParam1 = getArguments().getString(ARG_PARAM1);
            mParam2 = getArguments().getString(ARG_PARAM2);
        }

    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        View root = inflater.inflate(R.layout.fragment_registration, container, false);

        name = root.findViewById(R.id.regUsername);
        email = root.findViewById(R.id.regEmail);
        password = root.findViewById(R.id.regPassword);


        signup = root.findViewById(R.id.btnSignup);
        signup.setOnClickListener(this);


        return root;

    }

    // TODO: Rename method, update argument and hook method into UI event
    public void onButtonPressed(Uri uri) {
        if (mListener != null) {
            mListener.onFragmentInteraction(uri);
        }
    }

    @Override
    public void onAttach(Context context) {
        super.onAttach(context);
        if (context instanceof OnFragmentInteractionListener) {
            mListener = (OnFragmentInteractionListener) context;
        } else {
            throw new RuntimeException(context.toString()
                    + " must implement OnFragmentInteractionListener");
        }
    }

    @Override
    public void onDetach() {
        super.onDetach();
        mListener = null;
    }

    @Override
    public void onClick(View v) {
        if ((name.getText().toString().length()==0) ||(email.getText().toString().length()==0) || (password.getText().toString().length()==0)){

            Toast.makeText(getActivity(), "Please Enter All the Fields", Toast.LENGTH_SHORT).show();

        }
        else
        {
            signUp();
        }


    }


    private void signUp(){


        preferences= getActivity().getSharedPreferences("User", Context.MODE_PRIVATE);
        SharedPreferences.Editor mEditor = preferences.edit();
        mEditor.putString("regName", name.getText().toString());
        mEditor.putString("regEmail", email.getText().toString());
        mEditor.putString("regPassword", password.getText().toString());

        mEditor.commit();
//        mEditor.apply();

        Toast.makeText(getActivity(),"REGISTRATION SUCCESSFUL", Toast.LENGTH_SHORT).show();
        name.setText("");
        email.setText("");
        password.setText("");
    }
